type Translations = typeof translations.en | typeof translations.fr;

interface MobileLangContextValue {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: Translations;
}

const MobileLangContext = createContext<MobileLangContextValue>({
  lang: 'en',
  setLang: () => undefined,
  t: translations.en,
});

export function MobileLangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');

  useEffect(() => {
    const locales = ExpoLocalization.getLocales();
    const primary = locales[0]?.languageCode ?? 'en';
    setLangState(primary === 'fr' ? 'fr' : 'en');
  }, []);

  function setLang(newLang: Lang) {
    setLangState(newLang);
  }

  return (
    <MobileLangContext.Provider value={{ lang, setLang, t: translations[lang] }}>
      {children}
    </MobileLangContext.Provider>
  );
}

export function useMobileLang() {
  return useContext(MobileLangContext);
}
