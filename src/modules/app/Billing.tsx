function printInvoice(invoice: any, client: any, lang: string) {
  const locale = lang === 'fr' ? 'fr-FR' : 'en-US';
  const fmtCurrency = (n: number) =>
    new Intl.NumberFormat(locale, { style: 'currency', currency: 'USD', maximumFractionDigits: 0 }).format(n);

  const rows = (invoice.items ?? []).map((item: any) => `
    <tr>
      <td>${item.description}</td>
      <td style="text-align:center">${item.quantity}</td>
      <td style="text-align:right">${fmtCurrency(item.price)}</td>
      <td style="text-align:right">${fmtCurrency(item.quantity * item.price)}</td>
    </tr>`).join('');

  const html = `<!DOCTYPE html><html><head><title>${invoice.invoiceNumber}</title>
  <style>
    body{font-family:Inter,system-ui,sans-serif;color:#111;padding:48px;max-width:780px;margin:0 auto}
    h1{font-size:28px;margin-bottom:4px}
    .meta{color:#555;font-size:13px;margin-bottom:32px}
    table{width:100%;border-collapse:collapse;margin-top:24px}
    th{border-bottom:2px solid #111;padding:8px 4px;text-align:left;font-size:12px;text-transform:uppercase;letter-spacing:.05em}
    td{padding:8px 4px;font-size:13px;border-bottom:1px solid #eee}
    .total-row td{font-weight:600;border-top:2px solid #111;border-bottom:none}
    .footer{margin-top:48px;font-size:12px;color:#777}
    @media print{body{padding:0}}
  </style></head><body>
  <h1>${invoice.invoiceNumber}</h1>
  <div class="meta">
    <div><strong>${client?.company ?? ''}</strong></div>
    <div>Date: ${new Date(invoice.date).toLocaleDateString(locale)}</div>
    <div>Due: ${new Date(invoice.dueDate).toLocaleDateString(locale)}</div>
    <div>Status: ${invoice.status}</div>
  </div>
  <table>
    <thead><tr><th>Description</th><th style="text-align:center">Qty</th><th style="text-align:right">Unit price</th><th style="text-align:right">Total</th></tr></thead>
    <tbody>${rows}</tbody>
    <tfoot><tr class="total-row"><td colspan="3" style="text-align:right">Total</td><td style="text-align:right">${fmtCurrency(invoice.amount)}</td></tr></tfoot>
  </table>
  <div class="footer">Generated by Minerva OS · Uprising Studio</div>
  <script>window.onload=()=>{window.print();}</script>
  </body></html>`;

  const win = window.open('', '_blank');
  if (!win) return;
  win.document.write(html);
  win.document.close();
}


function fmt(n: number, lang: string) {
  return new Intl.NumberFormat(lang === 'fr' ? 'fr-FR' : 'en-US', { 
    style: 'currency', 
    currency: 'USD', 
    maximumFractionDigits: 0 
  }).format(n);
}

function InvoiceRow({ invoice, t, lang, clients }: { invoice: any; t: any; lang: string, clients: any[] }) {
  const [expanded, setExpanded] = useState(false);
  const [generatingLink, setGeneratingLink] = useState(false);
  const [linkFeedback, setLinkFeedback] = useState<'copied' | 'error' | null>(null);
  const b = t.app.billing;
  const common = t.app.common;
  
  const client = clients.find((c: any) => c._id === invoice.clientId);

  async function handlePaymentLink() {
    setGeneratingLink(true);
    setLinkFeedback(null);
    try {
      const res = await fetch('/api/stripe/checkout', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          invoiceId: invoice._id,
          amount: invoice.amount,
          currency: 'USD',
          clientEmail: client?.email ?? '',
        }),
      });
      const data = await res.json();
      if (data.error) throw new Error(data.error);
      await navigator.clipboard.writeText(data.url);
      setLinkFeedback('copied');
      setTimeout(() => setLinkFeedback(null), 2500);
    } catch {
      setLinkFeedback('error');
      setTimeout(() => setLinkFeedback(null), 2500);
    } finally {
      setGeneratingLink(false);
    }
  }

  const STATUS_CONFIG: Record<string, { label: string; class: string }> = {
    draft:     { label: common.status.draft,     class: 'text-fog bg-fog/10 border-fog/20'       },
    sent:      { label: common.status.pending,   class: 'text-warm bg-warm/10 border-warm/20'    },
    overdue:   { label: common.status.overdue,   class: 'text-ember bg-ember/10 border-ember/20' },
    paid:      { label: common.status.paid,      class: 'text-sage bg-sage/10 border-sage/20'    },
    cancelled: { label: common.status.rejected, class: 'text-fog bg-fog/5 border-fog/15'        },
  };

  const sc = STATUS_CONFIG[invoice.status] || STATUS_CONFIG.draft;

  return (
    <div
      className="rounded-xl border overflow-hidden transition-colors duration-200"
      style={{ backgroundColor: 'rgba(255,255,255,0.02)', borderColor: 'rgba(255,255,255,0.06)' }}
    >
      <div
        className="flex items-center gap-3 px-4 py-3 cursor-pointer hover:bg-white/[0.015]"
        onClick={() => setExpanded(e => !e)}
      >
        {/* Number */}
        <div className="w-24 sm:w-32 shrink-0">
          <p className="text-sm font-semibold text-ivory tabular-nums">{invoice.invoiceNumber}</p>
        </div>

        {/* Client + project */}
        <div className="flex-1 min-w-0">
          <p className="text-sm text-ivory truncate">{client?.company || '...'}</p>
        </div>

        {/* Status */}
        <span className={cn('text-[10px] font-medium px-2 py-0.5 rounded-full border shrink-0', sc.class)}>
          {sc.label}
        </span>

        {/* Dates */}
        <div className="hidden md:block text-right shrink-0">
          <p className="text-xs text-fog">
            {invoice.status === 'paid'
              ? `${b.invoices.paid} ${new Date(invoice.date).toLocaleDateString(lang === 'fr' ? 'fr-FR' : 'en-GB', { day: 'numeric', month: 'short' })}`
              : `${b.invoices.due} ${new Date(invoice.dueDate).toLocaleDateString(lang === 'fr' ? 'fr-FR' : 'en-GB', { day: 'numeric', month: 'short' })}`}
          </p>
        </div>

        {/* Amount */}
        <p className="text-sm font-semibold text-ivory tabular-nums shrink-0 w-20 text-right">
          {fmt(invoice.amount, lang)}
        </p>

        {/* Expand toggle */}
        <div className="text-fog shrink-0">
          {expanded ? <ChevronUp size={14} /> : <ChevronDown size={14} />}
        </div>
      </div>

      {/* Line items */}
      <AnimatePresence>
        {expanded && (
          <motion.div
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: 'auto', opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={{ duration: 0.25, ease: [0.22, 1, 0.36, 1] }}
            className="overflow-hidden"
          >
            <div className="px-4 pb-3 pt-1" style={{ borderTop: '1px solid rgba(255,255,255,0.05)' }}>
              <div className="overflow-x-auto">
              <table className="w-full text-xs">
                <thead>
                  <tr className="text-fog uppercase tracking-wider">
                    <th className="text-left py-1.5 font-medium">{b.table.description}</th>
                    <th className="text-right py-1.5 font-medium w-12">{b.table.qty}</th>
                    <th className="text-right py-1.5 font-medium w-24">{b.table.unitPrice}</th>
                    <th className="text-right py-1.5 font-medium w-24">{b.table.total}</th>
                  </tr>
                </thead>
                <tbody>
                  {invoice.items.map((item: any, i: number) => (
                    <tr key={i} className="border-t" style={{ borderColor: 'rgba(255,255,255,0.04)' }}>
                      <td className="py-1.5 text-silver">{item.description}</td>
                      <td className="py-1.5 text-right text-fog tabular-nums">{item.quantity}</td>
                      <td className="py-1.5 text-right text-fog tabular-nums">{fmt(item.price, lang)}</td>
                      <td className="py-1.5 text-right text-silver tabular-nums font-medium">{fmt(item.quantity * item.price, lang)}</td>
                    </tr>
                  ))}
                </tbody>
                <tfoot>
                  <tr style={{ borderTop: '1px solid rgba(255,255,255,0.08)' }}>
                    <td colSpan={3} className="pt-2 text-right text-fog font-medium">Total</td>
                    <td className="pt-2 text-right text-ivory font-semibold tabular-nums">{fmt(invoice.amount, lang)}</td>
                  </tr>
                </tfoot>
              </table>
              </div>

              {/* Invoice actions */}
              <div className="flex items-center gap-2 mt-3 pt-3" style={{ borderTop: '1px solid rgba(255,255,255,0.05)' }}>
                <button
                  onClick={() => printInvoice(invoice, client, lang)}
                  className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs text-fog hover:text-ivory transition-colors hover:bg-white/5"
                >
                  <FileDown size={12} />
                  {b.actions.exportPdf}
                </button>
                {invoice.status !== 'paid' && (
                  <button
                    onClick={handlePaymentLink}
                    disabled={generatingLink}
                    className={cn(
                      'flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs transition-colors',
                      linkFeedback === 'copied'
                        ? 'text-sage bg-sage/10'
                        : linkFeedback === 'error'
                          ? 'text-ember bg-ember/10'
                          : 'text-fog hover:text-ivory hover:bg-white/5'
                    )}
                  >
                    {generatingLink
                      ? <Loader2 size={12} className="animate-spin" />
                      : <Link2 size={12} />}
                    {linkFeedback === 'copied'
                      ? b.actions.paymentLinkCopied
                      : linkFeedback === 'error'
                        ? b.actions.paymentLinkError
                        : generatingLink
                          ? b.actions.generating
                          : b.actions.paymentLink}
                  </button>
                )}
              </div>
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  );
}

