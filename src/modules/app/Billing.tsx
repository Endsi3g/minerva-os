export default function Billing() {
  const { t, lang } = useLang();
  const b = t.app.billing;

  const workspaces = useWorkspaces();
  const workspaceId = workspaces?.[0]?.id;

  const [page, setPage] = useState(1);
  const pageSize = 50;

  const { data: invoices, count: totalInvoicesCount } = useInvoices(workspaceId, page, pageSize) || { data: null, count: 0 };
  const retainers = useRetainers(workspaceId);
  const clients = useClients(workspaceId);

  const [filter, setFilter] = useState<string | 'all'>('all');
  const [query, setQuery]   = useState('');
  const [retainerSheetOpen, setRetainerSheetOpen] = useState(false);
  const [newRetainerForm, setNewRetainerForm] = useState({
    clientId: '',
    amount: '',
    cycle: 'monthly',
    hoursIncluded: '',
    startDate: new Date().toISOString().split('T')[0],
    notes: '',
  });

  const addRetainer = useAddRetainer();

  async function handleAddRetainer() {
    if (!newRetainerForm.clientId || !newRetainerForm.amount || !newRetainerForm.hoursIncluded) {
      toast.error(lang === 'fr' ? 'Veuillez remplir tous les champs obligatoires' : 'Please fill all required fields');
      return;
    }
    try {
      const start = new Date(newRetainerForm.startDate);
      if (newRetainerForm.cycle === 'monthly') {
        start.setMonth(start.getMonth() + 1);
      } else if (newRetainerForm.cycle === 'quarterly') {
        start.setMonth(start.getMonth() + 3);
      } else if (newRetainerForm.cycle === 'annual') {
        start.setFullYear(start.getFullYear() + 1);
      }
      const renewalDate = start.toISOString().split('T')[0];

      await addRetainer({
        workspaceId,
        clientId: newRetainerForm.clientId,
        amount: parseFloat(newRetainerForm.amount),
        cycle: newRetainerForm.cycle,
        status: 'active',
        startDate: newRetainerForm.startDate,
        renewalDate,
        hoursIncluded: parseFloat(newRetainerForm.hoursIncluded),
        hoursUsed: 0,
        notes: newRetainerForm.notes || undefined,
      });
      toast.success(lang === 'fr' ? 'Forfait ajouté avec succès' : 'Retainer added successfully');
      setRetainerSheetOpen(false);
      setNewRetainerForm({
        clientId: '',
        amount: '',
        cycle: 'monthly',
        hoursIncluded: '',
        startDate: new Date().toISOString().split('T')[0],
        notes: '',
      });
    } catch (e) {
      console.error(e);
      toast.error(lang === 'fr' ? "Erreur lors de la création du forfait" : "Failed to create retainer");
    }
  }

  const FILTER_TABS = useMemo(() => [
    { id: 'all' as const,     label: t.app.tasks.filters.all },
    { id: 'sent' as const,    label: t.app.common.status.pending },
    { id: 'overdue' as const, label: t.app.common.status.overdue },
    { id: 'draft' as const,   label: t.app.common.status.draft },
    { id: 'paid' as const,    label: t.app.common.status.paid },
  ], [t]);

  const isLoading = invoices === null || retainers === null || clients === null;
  const outstanding = invoices ? invoices.filter((i: any) => i.status === 'sent' || i.status === 'overdue').reduce((s: any, i: any) => s + i.amount, 0) : 0;
  const paidMTD     = invoices ? invoices.filter((i: any) => i.status === 'paid').reduce((s: any, i: any) => s + i.amount, 0) : 0;
  const overdueCount = invoices ? invoices.filter((i: any) => i.status === 'overdue').length : 0;
  const activeRetainersCount = retainers ? retainers.filter((r: any) => r.status === 'active').length : 0;
  const totalPages = Math.ceil(totalInvoicesCount / pageSize);

  const visible = invoices ? invoices.filter((i: any) => {
    const matchFilter = filter === 'all' || i.status === filter;
    const matchQuery  = query === '' ||
      i.invoiceNumber.toLowerCase().includes(query.toLowerCase());
    return matchFilter && matchQuery;
  }) : [];

  return (
    <>
      {/* Header */}
      <div className="flex flex-col sm:flex-row sm:items-center justify-between gap-4 mb-6">
        <div>
          <TextAnimate text={b.title} type="calmInUp" className="text-2xl font-semibold text-ivory" />
          <p className="text-sm text-fog mt-0.5">
            {b.stats
              .replace('invoices', String(totalInvoicesCount))
              .replace('active retainers', String(activeRetainersCount))}
          </p>
        </div>
        <div className="flex items-center gap-2 flex-wrap">
          <Button size="sm" variant="outline" onClick={() => setRetainerSheetOpen(true)} id="btn-new-retainer">
            <Plus size={14} />
            {b.retainers.newRetainer}
          </Button>
          <Button size="sm" id="btn-new-invoice">
            <Plus size={14} />
            {b.newInvoice}
          </Button>
        </div>
      </div>

      {/* Summary strip */}
      <div className="grid grid-cols-1 sm:grid-cols-3 gap-4 mb-8">
        {isLoading ? (
          [1, 2, 3].map(i => <KpiSkeleton key={i} />)
        ) : (
          (([
            { label: b.summary.outstanding,   numericValue: outstanding,  format: (n: number) => fmt(n, lang), color: outstanding > 0 ? 'text-warm'  : 'text-sage', sub: b.summary.outstandingSub },
            { label: b.summary.overdue,       numericValue: overdueCount, color: overdueCount > 0      ? 'text-ember' : 'text-sage', sub: overdueCount > 0 ? b.summary.overdueSub : b.summary.overdueNone },
            { label: b.summary.collected,     numericValue: paidMTD,      format: (n: number) => fmt(n, lang), color: 'text-sage',   sub: b.summary.collectedSub },
          ] as Array<{ label: string; numericValue: number; format?: (n: number) => string; color: string; sub: string }>).map(s => (
            <div key={s.label} className="bg-card border border-border rounded-xl p-4">
              <p className={cn('text-2xl font-semibold tabular-nums', s.color)}>
                <AnimatedNumber value={s.numericValue} format={s.format ?? ((n) => String(Math.round(n)))} stiffness={80} damping={18} mass={0.5} />
              </p>
              <p className="text-xs text-fog mt-1">{s.label}</p>
              <p className="text-[10px] text-fog/60 mt-0.5">{s.sub}</p>
            </div>
          )))
        )}
      </div>

      {/* Retainers */}
      <section className="mb-8">
        <TextAnimate text={b.retainers.title} type="fadeIn" className="text-sm font-semibold text-ivory mb-3" />
        {isLoading ? (
          <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-3">
            {[1, 2, 3].map(i => <RetainerCardSkeleton key={i} />)}
          </div>
        ) : activeRetainersCount === 0 ? (
          <div className="flex flex-col items-center justify-center py-12 text-center gap-3 bg-midnight/20 rounded-xl border border-white/5 p-6">
            <p className="text-sm text-silver font-medium">{lang === 'fr' ? 'Aucun contrat de forfait' : 'No retainer agreements'}</p>
            <p className="text-xs text-fog max-w-xs">{b.retainers.empty}</p>
            <Button size="sm" variant="outline" onClick={() => setRetainerSheetOpen(true)} className="rounded-full">
              <Plus size={14} className="mr-1.5" />
              {b.retainers.newRetainer}
            </Button>
          </div>
        ) : (
          <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-3">
            {retainers?.filter((r: any) => r.status === 'active').map((ret: any) => {
              const client = clients?.find((c: any) => c._id === ret.clientId);
              const pct = ret.hoursIncluded > 0 ? Math.round((ret.hoursUsed / ret.hoursIncluded) * 100) : 0;
              const barColor = pct >= 100 ? '#A86A6A' : pct >= 80 ? '#B89B6A' : '#7FA38A';
              return (
                <div key={ret._id} className="rounded-xl border border-border bg-card p-4">
                  <div className="flex items-start justify-between mb-3">
                    <div>
                      <p className="text-sm font-medium text-ivory">{client?.company || '...'}</p>
                      <p className="text-[10px] text-fog capitalize">
                        {ret.cycle} · {b.retainers.renews} {new Date(ret.renewalDate).toLocaleDateString(lang === 'fr' ? 'fr-FR' : 'en-GB', { day: 'numeric', month: 'short' })}
                      </p>
                    </div>
                    <p className="text-sm font-semibold text-ivory tabular-nums">{fmt(ret.amount, lang)}</p>
                  </div>
                  {/* Hours bar */}
                  <div>
                    <div className="flex justify-between text-[10px] text-fog mb-1">
                      <span>{b.retainers.hours}</span>
                      <span>{ret.hoursUsed} / {ret.hoursIncluded}h</span>
                    </div>
                    <div className="w-full h-1 rounded-full overflow-hidden bg-dusk">
                      <div
                        className="h-full rounded-full transition-all duration-500"
                        style={{ width: `${Math.min(pct, 100)}%`, backgroundColor: barColor }}
                      />
                    </div>
                  </div>
                  {ret.notes && <p className="text-[11px] text-fog/60 mt-2 leading-relaxed">{ret.notes}</p>}
                </div>
              );
            })}
          </div>
        )}
      </section>

      {/* Invoices */}
      <section>
        <div className="flex items-center gap-3 mb-4 flex-wrap">
          {/* Filters */}
          <div className="flex items-center gap-1 mb-4 overflow-x-auto">
            {FILTER_TABS.map(f => (
              <button
                key={f.id}
                onClick={() => { setFilter(f.id); setPage(1); }}
                className={cn(
                  'relative px-3 py-1 rounded-lg text-xs font-medium transition-colors shrink-0',
                  filter === f.id ? 'text-ivory' : 'text-fog hover:text-silver'
                )}
              >
                {filter === f.id && (
                  <motion.span
                    layoutId="billing-filter-pill"
                    className="absolute inset-0 rounded-lg"
                    style={{ backgroundColor: 'rgba(255,255,255,0.07)' }}
                    transition={{ type: 'spring', bounce: 0.2, duration: 0.35 }}
                  />
                )}
                <span className="relative z-10">{f.label}</span>
              </button>
            ))}
          </div>

          {/* Search */}
          <div className="relative">
            <Search size={13} className="absolute left-3 top-1/2 -translate-y-1/2 text-fog pointer-events-none" />
            <input
              type="text"
              placeholder={b.invoices.searchPlaceholder}
              value={query}
              onChange={e => setQuery(e.target.value)}
              className="pl-8 pr-3 py-1.5 rounded-lg text-xs text-ivory placeholder:text-fog outline-none transition-colors"
              style={{
                backgroundColor: 'rgba(255,255,255,0.04)',
                border: '1px solid rgba(255,255,255,0.08)',
                fontFamily: "'Inter', sans-serif",
              }}
            />
          </div>
        </div>

        <div className="space-y-2">
          {isLoading ? (
            <div className="space-y-2 py-4">
              {[1, 2, 3].map(i => <InvoiceRowSkeleton key={i} />)}
            </div>
          ) : visible.length === 0 ? (
            <div className="flex flex-col items-center justify-center py-12 text-center gap-3 bg-midnight/20 rounded-xl border border-white/5 p-6">
              <p className="text-sm text-silver font-medium">{lang === 'fr' ? 'Aucune facture' : 'No invoices'}</p>
              <p className="text-xs text-fog max-w-xs">{b.invoices.empty}</p>
            </div>
          ) : (
            <>
              {visible.map((inv: any) => <InvoiceRow key={inv._id} invoice={inv} t={t} lang={lang} clients={clients} />)}
              
              {totalPages > 1 && (
                <div className="flex items-center justify-between mt-4 px-1 py-2 border-t border-white/5">
                  <span className="text-xs text-fog">
                    Showing {Math.min((page - 1) * pageSize + 1, totalInvoicesCount)}-{Math.min(page * pageSize, totalInvoicesCount)} of {totalInvoicesCount} invoices
                  </span>
                  <div className="flex items-center gap-2">
                    <Button
                      size="sm"
                      variant="outline"
                      onClick={() => setPage(p => Math.max(p - 1, 1))}
                      disabled={page === 1}
                      className="border-white/10 text-fog hover:text-ivory"
                    >
                      Previous
                    </Button>
                    <span className="text-xs text-silver font-medium font-mono px-2">{page} / {totalPages}</span>
                    <Button
                      size="sm"
                      variant="outline"
                      onClick={() => setPage(p => Math.min(p + 1, totalPages))}
                      disabled={page === totalPages}
                      className="border-white/10 text-fog hover:text-ivory"
                    >
                      Next
                    </Button>
                  </div>
                </div>
              )}
            </>
          )}
        </div>
      </section>

      {/* Add Retainer Sheet */}
      <Sheet open={retainerSheetOpen} onOpenChange={setRetainerSheetOpen}>
        <SheetContent side="right" className="w-full sm:w-96 p-6 flex flex-col gap-6">
          <SheetHeader>
            <SheetTitle>{b.retainers.newRetainer}</SheetTitle>
          </SheetHeader>

          <div className="flex flex-col gap-4 flex-1">
            <div className="space-y-1.5">
              <Label>Client</Label>
              <Select
                value={newRetainerForm.clientId}
                onValueChange={v => setNewRetainerForm(f => ({ ...f, clientId: v }))}
              >
                <SelectTrigger>
                  <SelectValue placeholder={lang === 'fr' ? 'Sélectionner un client' : 'Select a client'} />
                </SelectTrigger>
                <SelectContent>
                  {clients?.map((c: any) => (
                    <SelectItem key={c._id} value={c._id}>
                      {c.company}
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
            </div>

            <div className="space-y-1.5">
              <Label>{lang === 'fr' ? 'Montant (USD)' : 'Amount (USD)'}</Label>
              <Input
                type="number"
                placeholder="3000"
                value={newRetainerForm.amount}
                onChange={e => setNewRetainerForm(f => ({ ...f, amount: e.target.value }))}
              />
            </div>

            <div className="space-y-1.5">
              <Label>{b.retainers.cycle}</Label>
              <Select
                value={newRetainerForm.cycle}
                onValueChange={v => setNewRetainerForm(f => ({ ...f, cycle: v }))}
              >
                <SelectTrigger>
                  <SelectValue />
                </SelectTrigger>
                <SelectContent>
                  <SelectItem value="monthly">{lang === 'fr' ? 'Mensuel' : 'Monthly'}</SelectItem>
                  <SelectItem value="quarterly">{lang === 'fr' ? 'Trimestriel' : 'Quarterly'}</SelectItem>
                  <SelectItem value="annual">{lang === 'fr' ? 'Annuel' : 'Annual'}</SelectItem>
                </SelectContent>
              </Select>
            </div>

            <div className="space-y-1.5">
              <Label>{b.retainers.hoursIncluded}</Label>
              <Input
                type="number"
                placeholder="20"
                value={newRetainerForm.hoursIncluded}
                onChange={e => setNewRetainerForm(f => ({ ...f, hoursIncluded: e.target.value }))}
              />
            </div>

            <div className="space-y-1.5">
              <Label>{lang === 'fr' ? 'Date de début' : 'Start Date'}</Label>
              <Input
                type="date"
                value={newRetainerForm.startDate}
                onChange={e => setNewRetainerForm(f => ({ ...f, startDate: e.target.value }))}
              />
            </div>

            <div className="space-y-1.5">
              <Label>{lang === 'fr' ? 'Notes (optionnel)' : 'Notes (optional)'}</Label>
              <Input
                placeholder="..."
                value={newRetainerForm.notes}
                onChange={e => setNewRetainerForm(f => ({ ...f, notes: e.target.value }))}
              />
            </div>
          </div>

          <Button className="w-full" onClick={handleAddRetainer}>
            {lang === 'fr' ? 'Créer le forfait' : 'Create Retainer'}
          </Button>
        </SheetContent>
      </Sheet>
    </>
  );
}

